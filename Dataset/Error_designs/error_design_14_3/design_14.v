module design_14(posit_in,//16bits
rc,
regime, //4bits
exp,//2bits
mant//14bits
);


parameter N=16;
parameter Bs=4; //4
parameter es = 2;

input [N-1:0] posit_in;//16bits
output rc;
output [Bs-1:0] regime; //4bits
output [es-1:0] exp;//2bits
output [N-es-1:0] mant;//14bits

wire [N-1:0] xin = posit_in;
assign rc = xin[N-2];

wire [N-1:0] xin_r = rc ? ~xin : xin;

wire [Bs-1:0] k;
LOD_N #(.N(N)) xinst_k(.in({xin_r[N-2:0],rc^1'b0}), .out(k));

assign regime = rc ? k-1 : k;

wire [N-1:0] xin_tmp;
DSR_left_N_S #(.N(Bs), .S(N)) ls (.a({xin[N-3:0],2'b0}),.b(k),.c(xin_tmp));

assign exp= xin_tmp[N-1:N-es];
assign mant= xin_tmp[N-es-1:0];

endmodule


module LOD_N (in, out);

parameter N = 64;
parameter S = 6; 
input [N-1:0] in;
output [S-1:0] out;

wire vld;
LOD #(.N(N)) l1 (in, out, vld);
endmodule
